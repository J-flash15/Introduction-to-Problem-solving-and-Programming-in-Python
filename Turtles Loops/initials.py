#Turtle loops


import turtle

# turtle module 

turtle = turtle.Turtle()

#turtle Shape 
    
turtle.shape("turtle")



#draws initials 

for _ in range(1):

    turtle.color("purple")

    turtle.pensize(8)

    turtle.penup()

    turtle.setpos(90, 185)

    turtle.pendown()

    turtle.circle(-100, -190)
    
    turtle.circle(100, -250)

    turtle.penup()

    turtle.setpos(90, 190)

    turtle.goto(90, 190)

    turtle.goto(90,350)

    turtle.left(50)

    turtle.fd(290)

    turtle.pendown()
    
    turtle.circle(-190, -200)

    turtle.right(90)

    turtle.fd(100)

    turtle.pendown()

    turtle.right(90)

    turtle.backward(100)
    
   


    

    

    

 

    
     
    

