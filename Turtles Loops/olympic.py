#Turtle loop



import turtle


#draw the blue ring 

for _ in range(1):


    tu = turtle.Turtle()
    tu.shape("turtle")


    tu.pensize(5)

    tu.color("blue")

    tu.penup()

    tu.back(90)

    tu.setheading(60)

    tu.pendown()

    tu.circle(75)

